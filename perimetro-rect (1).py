# Programa para calcular el área y el perímetro de un rectángulo

base = float(input("Escribe la base de tu rectángulo: "))
altura = float(input("Escribe la altura de tu rectángulo: "))


area = base * altura
perimetro = 2 * base + 2 * altura


print("El área de tu rectángulo es:", area)
print("El perímetro de tu rectángulo es:", perimetro)
